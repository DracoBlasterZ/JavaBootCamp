
public class Tar1_3 {
	public static void main(String[] args) {
		int X=13;
		int Y=31;
		double N=12.3;
		double M=25.67;
		
		System.out.println(X+" "+Y+" "+N+" "+M);
		System.out.println("X+Y es "+(X+Y));
		System.out.println("X-Y es "+(X-Y));
		System.out.println("X*Y es "+(X*Y));
		System.out.println("X/Y es "+(X/Y));
		System.out.println("X%Y es "+(X+Y));
		System.out.println("N+M es "+(N+M));
		System.out.println("N-M es "+(N-M));
		System.out.println("N*M es "+(N*M));
		System.out.println("N/M es "+(N/M));
		System.out.println("N%M es "+(N%M));
		System.out.println("El doble de todos es: "+(2*X)+" "+(2*Y)+" "+(2*N)+" "+(2*M));
		System.out.println("La suma de todas las variantes es: "+(X+Y+N+M));
		System.out.println("El modulo de todas las variables es "+(X%Y%N%M));
	
	}

}
